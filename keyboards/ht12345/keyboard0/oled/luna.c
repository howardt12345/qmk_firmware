void display_pet_parts(int LUNA_X, int LUNA_Y, const char data[PET_ROWS][ANIM_SIZE]) {
    for (int i = 0; i < PET_ROWS; ++i) {
        oled_set_cursor(LUNA_X, LUNA_Y + i);
        oled_write_raw_P(&data[i][0], ANIM_SIZE);
    }
}
/* animation */
void animate_luna(int LUNA_X, int LUNA_Y, int current_wpm, led_t led_usb_state) {
    /* jump */
    if (isJumping || !showedJump) {
        /* clear */
        oled_set_cursor(LUNA_X, LUNA_Y + 2);
        oled_write("     ", false);

        oled_set_cursor(LUNA_X, LUNA_Y - 1);

        showedJump = true;
    } else {
        /* clear */
        oled_set_cursor(LUNA_X, LUNA_Y - 1);
        oled_write("     ", false);

        oled_set_cursor(LUNA_X, LUNA_Y);
    }

    /* switch frame */
    current_frame = (current_frame + 1) % 2;

    /* current status */
    if (led_usb_state.caps_lock) {
        display_pet_parts(LUNA_X, LUNA_Y, bark[abs(1 - current_frame)]);

    } else if (isSneaking) {
        display_pet_parts(LUNA_X, LUNA_Y, sneak[abs(1 - current_frame)]);

    } else if (current_wpm <= MIN_WALK_SPEED) {
        display_pet_parts(LUNA_X, LUNA_Y, sit[abs(1 - current_frame)]);

    } else if (current_wpm <= MIN_RUN_SPEED) {
        display_pet_parts(LUNA_X, LUNA_Y, walk[abs(1 - current_frame)]);

    } else {
        display_pet_parts(LUNA_X, LUNA_Y, run[abs(1 - current_frame)]);
    }
}

/* logic */
static void render_luna(int LUNA_X, int LUNA_Y) {
    animate_luna(LUNA_X, LUNA_Y, get_current_wpm(), host_keyboard_led_state());
}

void draw_luna_ui(void) {
    led_t led_state = host_keyboard_led_state();
    draw_num_lock(53, 0, led_state);
    draw_caps_lock(79, 0, led_state);
    draw_scroll_lock(105, 0, led_state);
    draw_line_h(53, 15, 128, true);
    draw_keyboard_layers(56, 20, 6);
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        /* KEYBOARD PET STATUS START */
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
            /* KEYBOARD PET STATUS END */
    };
    return true;
}

